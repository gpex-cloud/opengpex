/**
 * cloud-sdk / auth / token-store
 *
 * Manages access token (in memory) and refresh token (localStorage).
 * Access token is never persisted — lost on page close.
 * Refresh token supports rotation detection.
 */

const REFRESH_TOKEN_KEY = "__gpex_refresh_token";

let accessToken: string | null = null;

export function getAccessToken(): string | null {
  return accessToken;
}

export function setAccessToken(token: string | null): void {
  accessToken = token;
}

export function getRefreshToken(): string | null {
  if (typeof window === "undefined") return null;
  try {
    return localStorage.getItem(REFRESH_TOKEN_KEY);
  } catch {
    return null;
  }
}

export function setRefreshToken(token: string | null): void {
  if (typeof window === "undefined") return;
  try {
    if (token) {
      localStorage.setItem(REFRESH_TOKEN_KEY, token);
    } else {
      localStorage.removeItem(REFRESH_TOKEN_KEY);
    }
  } catch {
    // localStorage unavailable (e.g. incognito mode full)
  }
}

export function clearTokens(): void {
  accessToken = null;
  setRefreshToken(null);
}
