import { GeometryService, PixelService, AssetService, LayerService, LayerUpdateTx, LayerEditor, asLocalShape, asLocalRect, asWorldShape, EditorActions, EditorData, Layer, VectorMask, BitmapMask, isPolygon } from '@opengpex/editor/core/types';
import { polygonToShape } from '@opengpex/editor/core/helpers/path2d';
import { getClipBox } from '@opengpex/editor/core/helpers/selection';
import { LayerFactory } from './LayerFactory';

/** Business convention: ID feature of hole masks generated by peeling */
// const MASK_ID_PEEL_HOLE = 'mask-peel-hole';

export function createLayerService(
  geometry: GeometryService,
  pixels: PixelService,
  assets: AssetService,
  actions: EditorActions,
  getState: () => EditorData
): LayerService {
  return {
    ...LayerFactory,

    updateLayer: (frameId, runner) => {
      const state = getState();
      const frame = state.frames.byId[frameId];
      if (!frame) return;

      const patches: Record<string, Partial<Layer>> = {};

      const tx: LayerUpdateTx = {
        edit: (layerId) => {
          const layer = frame.layers.byId[layerId];
          if (!layer) {
            console.warn(`[LayerTx] Layer ${layerId} not found in frame ${frameId}`);
          }

          // 🌟 Initialize vectorMasks in draft to avoid duplicate fallbacks later
          if (!patches[layerId]) {
            patches[layerId] = {};
          }
          if (patches[layerId].vectorMasks === undefined) {
            patches[layerId].vectorMasks = layer?.vectorMasks || [];
          }

          const editor: LayerEditor = {
            setAsset: (asset) => {
              patches[layerId] = { ...patches[layerId], src: asset.url, assetId: asset.id };
              return editor;
            },
            setShape: (shape) => {
              patches[layerId] = { ...patches[layerId], ...shape };
              return editor;
            },
            removeMask: (pattern) => {
              const currentMasks = patches[layerId].vectorMasks || [];
              const newMasks = currentMasks.filter((m: VectorMask) => !m.id.includes(pattern));
              patches[layerId].vectorMasks = newMasks;
              return editor;
            },
            applyMask: (shape, inverted = false, feather = 0) => {
              const currentMasks = patches[layerId].vectorMasks || [];
              const newMask = LayerFactory.getNewVectorMask(shape, inverted, feather);
              patches[layerId].vectorMasks = [...currentMasks, newMask];
              return editor;
            },
            patch: (data) => {
              patches[layerId] = { ...patches[layerId], ...data };
              return editor;
            },
            reset: () => {
              patches[layerId] = { ...LayerFactory.getBlank() };
              return editor;
            },
            emptyLayer: () => {
              patches[layerId] = {
                ...patches[layerId],
                src: LayerFactory.TRANSPARENT_PIXEL,
                assetId: 'asset-transparent-pixel',
                vectorMasks: []
              };
              return editor;
            },
            maskLayer: () => {
              const currentLayer = layer;
              if (!currentLayer) return editor;

              const fullMask = LayerFactory.getNewVectorMask(
                asLocalShape({ x: 0, y: 0, w: currentLayer.bounding.w, h: currentLayer.bounding.h }, 'rect'),
                true
              );

              patches[layerId].vectorMasks = [...(patches[layerId].vectorMasks || []), fullMask];
              return editor;
            },
            resetWithBounds: (w, h, cx, cy) => {
              const { rotation, flip, opacity, adjustments, vectorMasks } = LayerFactory.getNewLayer();
              patches[layerId] = {
                ...patches[layerId],
                cx, cy,
                bounding: { w, h },
                visibleShape: asLocalShape({ x: 0, y: 0, w, h }),
                scale: 1, rotation, flip, opacity, adjustments, vectorMasks,
              };
              return editor;
            },
            setOpacity: (opacity) => {
              patches[layerId] = { ...patches[layerId], opacity };
              return editor;
            },
            setVisible: (visible) => {
              patches[layerId] = { ...patches[layerId], visible };
              return editor;
            },

            // --- Bitmap Mask operations ---
            applyBitmapMask: (src, assetId, bounds) => {
              const currentBitmapMasks = patches[layerId].bitmapMasks || layer?.bitmapMasks || [];
              const newMask = LayerFactory.getNewBitmapMask(src, assetId, bounds);
              patches[layerId].bitmapMasks = [...currentBitmapMasks, newMask];
              return editor;
            },
            removeBitmapMask: (maskId) => {
              const currentBitmapMasks = patches[layerId].bitmapMasks || layer?.bitmapMasks || [];
              patches[layerId].bitmapMasks = currentBitmapMasks.filter((m: BitmapMask) => m.id !== maskId);
              return editor;
            },
            updateBitmapMask: (maskId, maskPatch) => {
              const currentBitmapMasks = patches[layerId].bitmapMasks || layer?.bitmapMasks || [];
              patches[layerId].bitmapMasks = currentBitmapMasks.map((m: BitmapMask) =>
                m.id === maskId ? { ...m, ...maskPatch } : m
              );
              return editor;
            }
          };
          return editor;
        }
      };

      // Execute transaction logic
      runner(tx);

      // --- Core Enhancement: Cascade Calculation ---
      const finalPatches: Record<string, Partial<Layer>> = {};

      const layersArray = frame.layers.order.map(id => frame.layers.byId[id]);
      Object.entries(patches).forEach(([layerId, patch]) => {
        const cascade = LayerFactory.getLayerCascadePatches(layersArray, layerId, patch);
        Object.assign(finalPatches, cascade);
      });

      // Summarize and commit
      if (Object.keys(finalPatches).length > 0) {
        actions.batchUpdateLayers(frameId, finalPatches);
      }
    },



    addLayer: (frameId, layer, index) => {
      const expanded = LayerFactory.expandLayers([layer]);
      actions.addLayers(frameId, expanded, index);
    },

    addFrame: (frame, switchFrame = true) => {
      actions.addFrame(frame, switchFrame);
    },

    activate: (frameId, layerId) => {
      if (layerId) {
        const state = getState();
        const frame = state.frames.byId[frameId];
        const layer = frame?.layers.byId[layerId];
        if (layer && !LayerFactory.canLayerBeActivated(layer)) return;
      }
      actions.setActiveLayer(frameId, layerId);
    },

    // Upgraded version: supports passing either a single ID or an array of IDs (multi-selection delete)
    removeLayers: (frameId: string, layerIds: string | string[]) => {
      const frame = getState().frames.byId[frameId];
      if (!frame) return;

      const targetIds = Array.isArray(layerIds) ? layerIds : [layerIds];
      const allLayers = frame.layers.order.map(id => frame.layers.byId[id]);
      const idsToRemove = LayerFactory.collectDescendants(targetIds, allLayers);

      // Focus migration: pick nearest host layer if active is being removed
      let nextActiveId: string | null = frame.activeLayerId ?? null;
      if (!nextActiveId || idsToRemove.has(nextActiveId)) {
        const candidates = LayerFactory.getHostLayers(allLayers.filter(l => !idsToRemove.has(l.id)));
        const targetIndex = frame.layers.order.indexOf(targetIds[0]);
        const safeIndex = Math.min(targetIndex, candidates.length - 1);
        nextActiveId = candidates[safeIndex >= 0 ? safeIndex : 0]?.id ?? null;
      }

      actions.removeLayers(frameId, Array.from(idsToRemove), nextActiveId);
    },

    removeFrame: (frameId: string) => {
      const state = getState();
      const allFrames = state.frames.order.map(id => state.frames.byId[id]);
      const idsToRemove = LayerFactory.collectDescendants([frameId], allFrames);

      // Focus migration: pick first surviving frame
      let nextActiveFrameId: string | null = state.activeFrameId ?? null;
      if (nextActiveFrameId && idsToRemove.has(nextActiveFrameId)) {
        nextActiveFrameId = allFrames.find(f => !idsToRemove.has(f.id))?.id ?? null;
      }

      actions.removeFrame(Array.from(idsToRemove), nextActiveFrameId);
    },

    getTriplet: (frameId, layerId) => {
      const state = getState();
      const frame = state.frames.byId[frameId];
      const layer = frame?.layers.byId[layerId];
      if (!layer || !frame) return null;

      return LayerFactory.getTriplet(layer, frame.layers.order.map(id => frame.layers.byId[id]));
    },
    fragmentToLayerLogical: (frame, layer, nameType) => {
      const box = getClipBox(frame);
      if (!box) return null;
      const localShape = !box.regular
        ? polygonToShape(geometry.polygon.frameLocalToLayerLocal(box.spatial, frame, layer))
        : geometry.shape.frameLocalToLayerLocal(box.spatial, frame, layer);
      const intersection = geometry.shape.intersectWithLayer(localShape, layer);

      if (!intersection) return null;

      const { id: _oldId, ...layerData } = layer;
      const newLayer = LayerFactory.getNewLayer({
        ...layerData,
        vectorMasks: layerData.vectorMasks?.filter(m => !m.reserved) || [],
        name: LayerFactory.getNewLayerName(frame.layers.order.map(id => frame.layers.byId[id]), nameType),
        hostId: undefined
      });

      const v = intersection.visibleShape.rect;
      newLayer.bounding = { w: v.w, h: v.h };
      newLayer.visibleShape = { ...intersection.visibleShape };
      // With the bounding-centered matrix (translate(cx,cy) * translate(-bw/2,-bh/2)),
      // content at (vx, vy) maps to world (cx + vx, cy + vy). To place the content
      // center at intersection.center, we solve: cx = center.x - vx, cy = center.y - vy.
      newLayer.cx = intersection.center.x - v.x;
      newLayer.cy = intersection.center.y - v.y;
      newLayer.birthCenter = { cx: newLayer.cx, cy: newLayer.cy }; // 💡 Record birth center to trigger the golden return guide line when dragging fragments

      return { newLayer, localShape };
    },

    resampleLayerPhysical: async (layer, scaleX, scaleY) => {
      try {
        const isUniform = Math.abs(scaleX - scaleY) < 0.001;

        if (isUniform) {
          const totalScaleX = scaleX;
          const totalScaleY = scaleY;
          const targetW = Math.max(1, Math.round(layer.bounding.w * totalScaleX));
          const targetH = Math.max(1, Math.round(layer.bounding.h * totalScaleY));

          let url = layer.src;
          let id = layer.assetId;

          if (layer.src && layer.src !== LayerFactory.TRANSPARENT_PIXEL) {
            const dim = await pixels.decode.dimensions(layer.src);
            const targetSrcW = Math.max(1, Math.round(dim.w * totalScaleX));
            const targetSrcH = Math.max(1, Math.round(dim.h * totalScaleY));

            const newBlob = await pixels.process.resample(layer.src, { targetSize: { w: targetSrcW, h: targetSrcH } });
            id = await assets.register(newBlob);
            url = assets.getURL(id)!;

            await pixels.decode.htmlImage(url);
          }

          const updatedVisibleShape = layer.visibleShape ? {
            ...layer.visibleShape,
            rect: asLocalRect({
              x: layer.visibleShape.rect.x * totalScaleX,
              y: layer.visibleShape.rect.y * totalScaleY,
              w: layer.visibleShape.rect.w * totalScaleX,
              h: layer.visibleShape.rect.h * totalScaleY
            })
          } : undefined;

          const updatedMasks = layer.vectorMasks?.map((mask: VectorMask) => ({
            ...mask,
            shape: {
              ...mask.shape,
              rect: asLocalRect({
                x: mask.shape.rect.x * totalScaleX,
                y: mask.shape.rect.y * totalScaleY,
                w: mask.shape.rect.w * totalScaleX,
                h: mask.shape.rect.h * totalScaleY
              })
            }
          }));

          const patch: Partial<Layer> = {
            src: url,
            assetId: id,
            cx: layer.cx * scaleX,
            cy: layer.cy * scaleY,
            bounding: { w: targetW, h: targetH },
            scale: 1,
            ...(updatedVisibleShape && { visibleShape: updatedVisibleShape }),
            ...(updatedMasks && { vectorMasks: updatedMasks })
          };

          return { newUrl: url, newAssetId: id || '', patch };
        } else {
          const aabb = geometry.space.getLayerBoundingBox(layer);

          let finalUrl = layer.src;
          let finalId = layer.assetId;
          const targetW = Math.max(1, Math.round(aabb.w * scaleX));
          const targetH = Math.max(1, Math.round(aabb.h * scaleY));

          if (layer.src && layer.src !== LayerFactory.TRANSPARENT_PIXEL) {
            const bakedBlobResult = await pixels.worker.mergeLayersWithShape([layer], asWorldShape(aabb, 'rect'));
            const bakedAsset = await pixels.worker.asAsset(Promise.resolve(bakedBlobResult));

            const newBlob = await pixels.process.resample(bakedAsset.url, { targetSize: { w: targetW, h: targetH } });
            finalId = await assets.register(newBlob);
            finalUrl = assets.getURL(finalId)!;

            await pixels.decode.htmlImage(finalUrl);
          }

          const newCx = (aabb.x + aabb.w / 2) * scaleX;
          const newCy = (aabb.y + aabb.h / 2) * scaleY;

          const patch: Partial<Layer> = {
            src: finalUrl,
            assetId: finalId,
            cx: newCx,
            cy: newCy,
            bounding: { w: targetW, h: targetH },
            scale: 1,
            rotation: 0,
            flip: { h: false, v: false },
            visibleShape: asLocalShape({ x: 0, y: 0, w: targetW, h: targetH }),
            vectorMasks: []
          };

          return { newUrl: finalUrl, newAssetId: finalId || '', patch };
        }
      } catch (err) {
        console.error('[LayerService] Resample physical failed for layer:', layer.id, err);
        return null;
      }
    },

    fragmentToExistLayer: (frame, sourceLayer, targetLayer, selection) => {
      const localShape = isPolygon(selection)
        ? polygonToShape(geometry.polygon.frameLocalToLayerLocal(selection, frame, sourceLayer))
        : geometry.shape.frameLocalToLayerLocal(selection, frame, sourceLayer);
      const intersection = geometry.shape.intersectWithLayer(localShape, sourceLayer);

      if (!intersection) return null;

      const v = intersection.visibleShape.rect;

      const updatedLayer = {
        ...targetLayer,
        src: sourceLayer.src,
        assetId: sourceLayer.assetId,
        // Bounding-centered matrix compensation (same as fragmentToLayerLogical)
        cx: intersection.center.x - v.x,
        cy: intersection.center.y - v.y,
        scale: 1,
        rotation: sourceLayer.rotation,
        flip: { ...sourceLayer.flip },
        bounding: { w: v.w, h: v.h },
        visibleShape: { ...intersection.visibleShape },
        interactive: true,
        opacity: 1,
        visible: true
      };

      return { updatedLayer, localShape };
    },

    fragmentToLayerPhysical: async (frame, layer, nameType) => {
      const box = getClipBox(frame);
      if (!box) return null;
      const localShape = !box.regular
        ? polygonToShape(geometry.polygon.frameLocalToLayerLocal(box.spatial, frame, layer))
        : geometry.shape.frameLocalToLayerLocal(box.spatial, frame, layer);
      const intersection = geometry.shape.intersectWithLayer(localShape, layer);
      if (!intersection) return null;

      // For pixel rasterization, convert selection to world-space shape
      const frameLocalShape = !box.regular ? polygonToShape(box.spatial) : box.spatial;
      const worldSelection = geometry.shape.localToWorldShape(frameLocalShape, frame);
      const targetW = Math.round(worldSelection.rect.w);
      const targetH = Math.round(worldSelection.rect.h);

      const assetResult = await pixels.worker.asAsset(
        pixels.worker.mergeLayersWithShape([layer], worldSelection)
      );

      const trimmedRect = await pixels.decode.contentBounds(assetResult.url);

      if (trimmedRect.w <= 0 || trimmedRect.h <= 0) return null;

      const isFull = trimmedRect.x === 0 && trimmedRect.y === 0 && trimmedRect.w === targetW && trimmedRect.h === targetH;
      const finalAsset = isFull
        ? { id: assetResult.id, url: assetResult.url }
        : await pixels.worker.asAsset(pixels.worker.cloneRegion(assetResult.id, trimmedRect));

      const cx = worldSelection.rect.x + (trimmedRect.x + trimmedRect.w / 2);
      const cy = worldSelection.rect.y + (trimmedRect.y + trimmedRect.h / 2);

      const { id: _, ...layerData } = layer;
      const newLayer = LayerFactory.getNewLayer({
        ...layerData,
        src: finalAsset.url,
        assetId: finalAsset.id,
        vectorMasks: layerData.vectorMasks?.filter(m => !m.reserved) || [],
        name: LayerFactory.getNewLayerName(frame.layers.order.map(id => frame.layers.byId[id]), nameType),
        hostId: undefined,
        visibleShape: asLocalShape({ x: 0, y: 0, w: trimmedRect.w, h: trimmedRect.h }, worldSelection.type),
        bounding: { w: trimmedRect.w, h: trimmedRect.h },
        cx,
        cy,
        birthCenter: { cx, cy }, // 💡 Record birth center to trigger the golden return guide line when dragging fragments
        scale: 1,
        rotation: 0,
        flip: { h: false, v: false },
        adjustments: { brightness: 100, contrast: 100, saturation: 100, hueRotate: 0, blur: 0 }
      });

      return {
        newLayer,
        localShape,
        url: finalAsset.url
      };
    },
    createLayerFromBlob: async (blob, frame, screenPoint) => {
      const assetId = await assets.register(blob);
      const url = assets.getURL(assetId)!;
      const dim = await pixels.decode.dimensions(url);

      // layer.cx/cy uses the world coordinate system (origin at canvas center), directly using the screenToWorld result
      let cx, cy;
      if (screenPoint) {
        const worldPos = geometry.space.screenToWorld(screenPoint.x, screenPoint.y, frame);
        cx = worldPos.x;
        cy = worldPos.y;
      } else {
        const state = getState();
        const vDim = state.ui.viewportDim;
        const worldCenter = geometry.space.screenToWorld(vDim.w / 2, vDim.h / 2, frame);
        cx = worldCenter.x;
        cy = worldCenter.y;
      }

      const layersArray = frame.layers.order.map(id => frame.layers.byId[id]);
      const smartName = LayerFactory.getNewLayerName(layersArray, 'Pasted');

      return LayerFactory.getNewLayer({
        name: smartName,
        src: url,
        assetId: assetId,
        cx,
        cy,
        bounding: dim,
        visibleShape: asLocalShape({ x: 0, y: 0, w: dim.w, h: dim.h }),
        birthCenter: { cx, cy }
      });
    },
  };
}

